Los reporte de producción contienen como datos:
<ul>
<li>Fecha, Numero de sierra cinta, Operador, Ayudante, Quien recibió y quién entregó
<li>la producción ralizada
	<ul>
	<li>Aserrio
	<li>Hojeado
	<li>Clavado de tarimas
	<li>Otros destajos
	</ul>
</ul>
Pasos a seguir:
<ul>
<li>Crear Nuevo Reporte (Definir los datos generales del reporte)
<li>Revisar los detalles (agregar/borrar actividades productivas realizadas)
</ul>
Puede haber uno o mas reportes por día y por sierra.
