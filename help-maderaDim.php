La madera dimensionada son tablas de pino u otra especie que especifican grueso, ancho y largo de la tabla (en pulgadas, centimetros, pies o metros)<br><br>
Se usan en la construcción de tarimas y son resultado de actividades productivas como el aserrio y el hojeado
<ul>
<li>Se pueden hacer busquedas por grueso
<li>o agregar una nueva definición de madera que no se tenga
	<ul>
	<li>Se deben dar las dimensiones de manera normal como
		<ul>
		<li>3/4x4x8 1/4
		<li>1 1/2 X 2 x 45
		<li>1+1/2 * 2 x 45
		<li>2.5* 10*1.50
		</ul>
	<li>El sistema intentará convertir a decimal y propone unidades
	<li>Ud puede corregir los datos decimales, unidades y volumen unitario si no son correctos
	<li><b>Pero</b> las Descripción (dimensiones) mostradas se usarán para las búsquedas posteriores (p.e. en producción o en construcción de tarimas)
	</ul>
<li>Se pueden agregar entradas (p.e. compras de madera aserrada) o salidas (p.e. mermas o ventas directas)
</ul>
